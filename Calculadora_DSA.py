def soma(valor1, valor2):
    suma = valor1 + valor2
    return  suma


def sustraçao(valor1, valor2):
    resta = valor1 - valor2
    return resta


def multiplicaçao(valor1, valor2):
    multiplicar = valor1 * valor2
    return multiplicar


def diviçao(valor1, valor2):
    dividir = valor1 / valor2
    return dividir